void program_byte_RTx(uint16_t adr, uint8_t data  )
{
    set_adr_RTx(adr);

    set_datain_pulldown();


    if (tester_mode == MODE_556RT5)
    {
        SET_PIN(DATA0 , data & 1);
        SET_PIN(DATA1 , (data >> 1) & 1);
        SET_PIN(DATA2 , (data >> 2) & 1);
        SET_PIN(DATA3 , (data >> 3) & 1);
        SET_PIN(DATA4 , (data >> 4) & 1);
        SET_PIN(DATA5 , (data >> 5) & 1);
        SET_PIN(DATA6 , (data >> 6) & 1);
        SET_PIN(DATA7 , (data >> 7) & 1);
        SET_PIN(CS1 , 1);
        SET_PIN(CS2 , 1);
        SET_PIN(CS3 , 0);
        SET_PIN(CS4 , 1);
        Delay_mks(10);
        SET_PIN(ON12V , 1);
        Delay_mks(10);
        SET_PIN(ON12V , 0);
        SET_PIN(CS3 , 1);
        Delay_mks(50);
    }
    else
    {
        data = ~data;
        SET_PIN(DATA0 , data & 1);
        SET_PIN(DATA1 , (data >> 1) & 1);
        SET_PIN(DATA2 , (data >> 2) & 1);
        SET_PIN(DATA3 , (data >> 3) & 1);
        if (tester_mode == MODE_556RT7)
        {
            SET_PIN(DATA4 , (data >> 4) & 1);
            SET_PIN(DATA5 , (data >> 5) & 1);
            SET_PIN(DATA6 , (data >> 6) & 1);
            SET_PIN(DATA7 , (data >> 7) & 1);
            SET_PIN(CS3 , 1);
        }

        if (tester_mode == MODE_556RT4)
        {
            SET_PIN(CS1 , 0);
            SET_PIN(CS2 , 0);
        }
        Delay_mks(10);
        SET_PIN(ON12V , 1);
        Delay_mks(10);
        SET_PIN(ON12V , 0);
        SET_PIN(CS1 , 1);
        SET_PIN(CS2 , 1);
        SET_PIN(CS3 , 1);
        Delay_mks(50);
    }

    Delay_mks(200000);
    if (tester_mode != MODE_556RT5) data = ~data;
    PrintInt("Adr=", adr, TX_CB);
    PrintInt(" Prg=", (uint8_t)data, TX_CB);

    set_datain_pullup();
    clear_data();
    uint8_t prg = read_byte_RTx(adr);
    PrintInt(" Data=", prg, TX_CB);
    if (((uint8_t)data) == prg)
        PrintText(" OK\n", TX_CB);
    else
        PrintText(" BAD\n", TX_CB);
}

void task_program_RTx(uint8_t tag)
{
    if (tag == 3)
    program_byte_RTx(write_adr,write_data);
    if (tag == 4)
    {
        if (tester_mode == MODE_556RT7) write_data = VectorData[write_adr];
        program_byte_RTx(write_adr,write_data);
        write_adr++;
        if (write_adr != bufsize)
        {
            task_runOnce(task_program_RTx,10,4);
        }

    }
}

uint8_t read_byte_RTx(uint16_t adr)
{
    uint8_t Data = 0;
    set_adr_RTx(adr);
    if (tester_mode == MODE_556RT4)
    {
        SET_PIN(CS1 , 0);
        SET_PIN(CS2 , 0);
    }
     if (tester_mode == MODE_556RT5)
    {
        SET_PIN(CS1 , 1);
        SET_PIN(CS2 , 1);
        SET_PIN(CS3 , 0);
        SET_PIN(CS4 , 0);
    }
    if (tester_mode == MODE_556RT7)
    {
        SET_PIN(CS1 , 1);
        SET_PIN(CS2 , 1);
        SET_PIN(CS3 , 0);
    }
    Delay_mks(100);
    Data |=  GET_PIN(DIN0);
    Data |=  GET_PIN(DIN1) << 1;
    Data |=  GET_PIN(DIN2) << 2;
    Data |=  GET_PIN(DIN3) << 3;
    Data |=  GET_PIN(DIN4) << 4;
    Data |=  GET_PIN(DIN5) << 5;
    Data |=  GET_PIN(DIN6) << 6;
    Data |=  GET_PIN(DIN7) << 7;
    SET_PIN(CS1 , 1);
    SET_PIN(CS2 , 1);
    if (tester_mode == MODE_556RT4)
    {
        Data &= 0x0F;
    }
    if (tester_mode == MODE_556RT5)
    {
        SET_PIN(CS3 , 1);
        SET_PIN(CS4 , 1);
    }
    if (tester_mode == MODE_556RT7)
    {
        SET_PIN(CS3 , 1);
    }

    return Data;
}


